/* We can use SWITCH...case syntax to implement decision behavior in our program. The following is a syntax model for switch..case.
￼￼switch (option) {
    case option1:
        // do option1 job
        break;
    case option2:
        // do option2 job
break; } */

var color = 'green';
switch(color) {
  case 'black':
    console.log('black');
    break;
  case 'green':
    console.log('green');
    break;
  case 'white':
    console.log('white');
    break;
}
